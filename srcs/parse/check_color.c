#include "minirt.h"

t_bool	valid_color(t_color3 c)
{
	return ((c.x >= 0.0 && c.x <= 255.0) && (c.y >= 0.0 && c.y <= 255.0) &&
			(c.z >= 0.0 && c.z <= 255.0));
}
