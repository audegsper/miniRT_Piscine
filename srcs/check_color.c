#include "minirt.h"

t_bool	valid_color(t_color c)
{
	return ((c.r >= 0.0 && c.r <= 255.0) && (c.g >= 0.0 && c.g <= 255.0) &&
			(c.b >= 0.0 && c.b <= 255.0));
}
